#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

OUTPUT_ROOT="${PROMPT_OPINION_E2E_OUTPUT_ROOT:-${REPO_ROOT}/output/prompt-opinion-e2e}"
RUN_ID="${PROMPT_OPINION_E2E_RUN_ID:-$(date -u +"%Y%m%dT%H%M%SZ")}"
SKIP_NPM_CI="${PROMPT_OPINION_SKIP_NPM_CI:-0}"
RUN_DIR="${OUTPUT_ROOT}/runs/${RUN_ID}"
LOG_DIR="${RUN_DIR}/logs"
REPORTS_DIR="${RUN_DIR}/reports"
NOTES_DIR="${RUN_DIR}/notes"
SCREENSHOTS_DIR="${RUN_DIR}/screenshots"
RESULTS_FILE="${REPORTS_DIR}/command-results.tsv"

mkdir -p "${LOG_DIR}" "${REPORTS_DIR}" "${NOTES_DIR}" "${SCREENSHOTS_DIR}"
: > "${RESULTS_FILE}"

BRANCH_NAME="$(git -C "${REPO_ROOT}" branch --show-current 2>/dev/null || echo unknown)"
COMMIT_SHA="$(git -C "${REPO_ROOT}" rev-parse HEAD 2>/dev/null || echo unknown)"

cat > "${REPORTS_DIR}/run-metadata.json" <<EOF
{
  "run_id": "${RUN_ID}",
  "generated_at": "$(date -u +"%Y-%m-%dT%H:%M:%SZ")",
  "branch": "${BRANCH_NAME}",
  "commit": "${COMMIT_SHA}",
  "repo_root": "${REPO_ROOT}",
  "output_root": "${OUTPUT_ROOT}",
  "run_dir": "${RUN_DIR}"
}
EOF

cat > "${SCREENSHOTS_DIR}/README.md" <<'EOF'
# Prompt Opinion screenshot slot

Store manual Prompt Opinion screenshots for this run in this folder.
Use this filename order:
- `01-po-login-page.png`
- `02-po-workspace-launchpad.png`
- `03-po-mcp-servers-registered.png`
- `04-po-a2a-connection-status.png`
- `05-po-byo-agent-config.png`
- `06-po-prompt1-result.png`
- `07-po-prompt2-result-or-stall.png`
- `08-po-prompt3-result-or-stall.png`

If `PROMPT_OPINION_BROWSER_CAPTURE=1` is used, the browser harness also writes:
- `a2a-p*-result.png`
- `fallback-p*-result.png`
- matching `.txt` DOM snapshots for each screenshot
EOF

seed_note_from_template() {
  local template_path="$1"
  local output_path="$2"

  if [[ ! -f "${template_path}" ]]; then
    return
  fi

  {
    cat <<EOF
<!-- Generated by run-prompt-opinion-rehearsal-capture.sh -->
<!-- Source template: ${template_path} -->

## Generated run context
- Run ID: \`${RUN_ID}\`
- Generated at (UTC): \`$(date -u +"%Y-%m-%dT%H:%M:%SZ")\`
- Branch: \`${BRANCH_NAME}\`
- Commit: \`${COMMIT_SHA}\`
- Run folder: \`${RUN_DIR}\`

---

EOF
    cat "${template_path}"
  } > "${output_path}"
}

seed_note_from_template \
  "${REPO_ROOT}/docs/templates/prompt-opinion-validation-notes-template.md" \
  "${NOTES_DIR}/validation-notes.md"

seed_note_from_template \
  "${REPO_ROOT}/docs/templates/prompt-opinion-workspace-evidence-template.md" \
  "${NOTES_DIR}/workspace-evidence.md"

seed_note_from_template \
  "${REPO_ROOT}/docs/templates/prompt-opinion-experiment-matrix-template.md" \
  "${NOTES_DIR}/experiment-matrix.md"

seed_note_from_template \
  "${REPO_ROOT}/docs/templates/prompt-opinion-request-id-correlation-template.md" \
  "${NOTES_DIR}/request-id-correlation.md"

overall_rc=0

cleanup() {
  "${SCRIPT_DIR}/stop-a2a-local.sh" >/dev/null 2>&1 || true
  "${SCRIPT_DIR}/stop-two-mcp-local.sh" >/dev/null 2>&1 || true
}

trap cleanup EXIT

run_cmd() {
  local label="$1"
  local slug="$2"
  local command="$3"
  local log_rel="logs/${slug}.log"
  local log_file="${RUN_DIR}/${log_rel}"
  local start_ms end_ms duration_ms status

  start_ms="$(node -p 'Date.now()')"
  if (cd "${REPO_ROOT}" && bash -lc "${command}") >"${log_file}" 2>&1; then
    status="green"
  else
    status="red"
    overall_rc=1
  fi
  end_ms="$(node -p 'Date.now()')"
  duration_ms="$((end_ms - start_ms))"

  printf "%s|%s|%s|%s|%s\n" \
    "${label}" \
    "${command}" \
    "${status}" \
    "${duration_ms}" \
    "${log_rel}" >> "${RESULTS_FILE}"

  echo "[po-rehearsal] ${label}: ${status} (${duration_ms}ms)"
}

echo "[po-rehearsal] run_id=${RUN_ID}"
echo "[po-rehearsal] branch=${BRANCH_NAME} commit=${COMMIT_SHA}"

if [[ "${SKIP_NPM_CI}" != "1" ]]; then
  run_cmd \
    "Install dependencies (Discharge Gatekeeper MCP)" \
    "00-npm-ci-typescript" \
    "npm --prefix po-community-mcp-main/typescript ci"

  run_cmd \
    "Install dependencies (Clinical Intelligence MCP)" \
    "00-npm-ci-clinical-intelligence" \
    "npm --prefix po-community-mcp-main/clinical-intelligence-typescript ci"

  run_cmd \
    "Install dependencies (external A2A orchestrator)" \
    "00-npm-ci-external-a2a" \
    "npm --prefix po-community-mcp-main/external-a2a-orchestrator-typescript ci"
fi

run_cmd \
  "Full system validation" \
  "01-run-full-system-validation" \
  "./po-community-mcp-main/scripts/run-full-system-validation.sh"

run_cmd \
  "Boot two MCP runtimes" \
  "02-start-two-mcp-local" \
  "./po-community-mcp-main/scripts/start-two-mcp-local.sh"

run_cmd \
  "Boot external A2A runtime" \
  "03-start-a2a-local" \
  "./po-community-mcp-main/scripts/start-a2a-local.sh"

run_cmd \
  "Two-MCP readiness check" \
  "04-check-two-mcp-readiness" \
  "./po-community-mcp-main/scripts/check-two-mcp-readiness.sh"

run_cmd \
  "A2A readiness check" \
  "05-check-a2a-readiness" \
  "./po-community-mcp-main/scripts/check-a2a-readiness.sh"

cleanup

run_cmd \
  "Prompt Opinion rehearsal smoke" \
  "06-smoke-prompt-opinion-rehearsal" \
  "PROMPT_OPINION_E2E_OUTPUT_DIR='${OUTPUT_ROOT}' PROMPT_OPINION_E2E_RUN_ID='${RUN_ID}' npm --prefix po-community-mcp-main/external-a2a-orchestrator-typescript run smoke:prompt-opinion-rehearsal"

if [[ "${PROMPT_OPINION_BROWSER_CAPTURE:-0}" == "1" ]]; then
  run_cmd \
    "Boot two MCP runtimes for browser proof" \
    "07-start-two-mcp-browser-proof" \
    "./po-community-mcp-main/scripts/start-two-mcp-local.sh"

  run_cmd \
    "Boot external A2A runtime for browser proof" \
    "08-start-a2a-browser-proof" \
    "./po-community-mcp-main/scripts/start-a2a-local.sh"

  run_cmd \
    "Authenticated Prompt Opinion browser proof" \
    "09-prompt-opinion-browser-proof" \
    "PROMPT_OPINION_E2E_RUN_DIR='${RUN_DIR}' PROMPT_OPINION_E2E_RUN_ID='${RUN_ID}' ./po-community-mcp-main/scripts/run-prompt-opinion-browser-proof.sh"

  cleanup
fi

node - "${RESULTS_FILE}" "${REPORTS_DIR}" "${RUN_ID}" "${BRANCH_NAME}" "${COMMIT_SHA}" <<'NODE'
const fs = require("node:fs");
const path = require("node:path");

const [resultsFile, reportsDir, runId, branchName, commitSha] = process.argv.slice(2);
const lines = fs
  .readFileSync(resultsFile, "utf8")
  .split("\n")
  .map((line) => line.trim())
  .filter(Boolean);

const rows = lines.map((line) => {
  const [label, command, status, durationMs, logFile] = line.split("|");
  return { label, command, status, durationMs: Number(durationMs), logFile };
});

const greenCount = rows.filter((row) => row.status === "green").length;
const redCount = rows.filter((row) => row.status === "red").length;
const localRows = rows.filter((row) => !/browser proof/i.test(row.label));
const localGreenCount = localRows.filter((row) => row.status === "green").length;
const localRedCount = localRows.filter((row) => row.status === "red").length;
const localLaneStatus = localRedCount === 0 ? "green" : "red";

const summary = {
  run_id: runId,
  generated_at: new Date().toISOString(),
  branch: branchName,
  commit: commitSha,
  checks: rows,
  totals: {
    green: greenCount,
    red: redCount,
  },
  local_totals: {
    green: localGreenCount,
    red: localRedCount,
  },
  lane_status_defaults: [
    {
      lane: "Local automated rehearsal lane",
      status: localLaneStatus,
      reason:
        localLaneStatus === "green"
          ? "All automated local checks passed in the current run folder."
          : "One or more automated local checks failed in the current run folder.",
    },
    {
      lane: "Prompt Opinion A2A-main workspace lane",
      status: "yellow",
      reason: "Manual workspace execution proof is required outside local smoke.",
    },
    {
      lane: "Prompt Opinion Direct-MCP fallback workspace lane",
      status: "yellow",
      reason: "Manual workspace transcript proof is required outside local smoke.",
    },
  ],
  manual_status_defaults: [
    {
      gate: "Prompt Opinion A2A chat execution",
      status: "yellow",
      reason: "Manual workspace execution proof is required outside local smoke.",
    },
    {
      gate: "Dual-tool BYO Prompt 2/3 transcript persistence",
      status: "yellow",
      reason: "Manual workspace transcript evidence is required outside local smoke.",
    },
  ],
};

fs.writeFileSync(path.join(reportsDir, "command-results.json"), JSON.stringify(summary, null, 2));

const browserProofSummaryPath = path.join(reportsDir, "browser-proof-summary.json");
const browserProofSummary = fs.existsSync(browserProofSummaryPath)
  ? JSON.parse(fs.readFileSync(browserProofSummaryPath, "utf8"))
  : null;
const a2aStatus = browserProofSummary?.lane_statuses?.a2a_main || "yellow";
const fallbackStatus = browserProofSummary?.lane_statuses?.direct_mcp_fallback || "yellow";
const a2aBlocker = browserProofSummary?.blockers?.a2a_main || "manual_browser_proof_required";
const fallbackBlocker = browserProofSummary?.blockers?.direct_mcp_fallback || "manual_browser_proof_required";
const registrationRows = (browserProofSummary?.registrations?.registrations || []).map((registration) =>
  `| ${registration.name} | ${registration.status.toUpperCase()} | ${registration.current_url_verified ? "yes" : "no"} | \`${registration.after_url || "missing"}\` |`,
);
const phase9Call =
  a2aStatus === "green" && fallbackStatus === "green"
    ? "GO"
    : fallbackStatus === "green"
      ? "CONDITIONAL GO"
      : "NO-GO";

const md = [
  "# Prompt Opinion Rehearsal Status",
  "",
  `Run ID: \`${runId}\``,
  "",
  "## Run metadata",
  "| Field | Value |",
  "| --- | --- |",
  `| Branch | \`${branchName}\` |`,
  `| Commit | \`${commitSha}\` |`,
  `| Run folder | \`output/prompt-opinion-e2e/runs/${runId}\` |`,
  "",
  "## Local automated checks",
  "| Check | Status | Duration (ms) | Log |",
  "| --- | --- | --- | --- |",
  ...rows.map(
    (row) =>
      `| ${row.label} | ${row.status.toUpperCase()} | ${row.durationMs} | \`${row.logFile}\` |`,
  ),
  "",
  "## Lane status board",
  "| Lane | Status | Reason |",
  "| --- | --- | --- |",
  `| Local automated rehearsal lane | ${localLaneStatus.toUpperCase()} | ${
    localLaneStatus === "green"
      ? "All automated local checks passed in this run folder."
      : "At least one automated local check failed in this run folder."
  } |`,
  `| Prompt Opinion A2A-main workspace lane | ${a2aStatus.toUpperCase()} | ${browserProofSummary ? `Authenticated browser blocker: ${a2aBlocker}.` : "Manual Prompt Opinion execution evidence is still required."} |`,
  `| Prompt Opinion Direct-MCP fallback workspace lane | ${fallbackStatus.toUpperCase()} | ${browserProofSummary ? `Authenticated browser blocker: ${fallbackBlocker}.` : "Manual Prompt Opinion transcript evidence is still required."} |`,
  "",
  "## Registration endpoint check",
  "| Surface | Status | Current URL verified | Active URL |",
  "| --- | --- | --- | --- |",
  ...(registrationRows.length ? registrationRows : ["| Not captured | YELLOW | no | `reports/registration-verification.json` missing |"]),
  "",
  "## Browser/network evidence",
  "| Artifact | Purpose |",
  "| --- | --- |",
  "| `reports/browser-proof-summary.json` | lane statuses, blockers, and prompt attempt summaries |",
  "| `reports/browser-network-events.json` | sanitized browser request/response event log |",
  "| `reports/browser-network-summary.json` | endpoint and request-shape summary |",
  "| `reports/runtime-log-delta.json` | A2A/MCP runtime log deltas captured during browser attempts |",
  "| `screenshots/` | authenticated workspace screenshots and prompt attempts |",
  "",
  "## Phase 9 call",
  `| Call | ${phase9Call} |`,
  "| --- | --- |",
  `| Basis | A2A-main=${a2aStatus}; Direct-MCP fallback=${fallbackStatus}; A2A blocker=${a2aBlocker}; fallback blocker=${fallbackBlocker} |`,
  "",
].join("\n");

fs.writeFileSync(path.join(reportsDir, "status-summary.md"), `${md}\n`);
NODE

ln -sfn "runs/${RUN_ID}" "${OUTPUT_ROOT}/latest"
cp "${REPORTS_DIR}/command-results.json" "${OUTPUT_ROOT}/command-results-latest.json"
cp "${REPORTS_DIR}/status-summary.md" "${OUTPUT_ROOT}/status-summary-latest.md"

if [[ "${overall_rc}" -ne 0 ]]; then
  echo "[po-rehearsal] FAIL: one or more automated checks are red"
  echo "[po-rehearsal] evidence bundle: ${RUN_DIR}"
  exit 1
fi

echo "[po-rehearsal] PASS: all automated checks are green"
echo "[po-rehearsal] evidence bundle: ${RUN_DIR}"
